function gameMenu() {
  var dropMenu = document.getElementById("dropMenu");

  if (dropMenu.title == "CLOSED") {
    dropMenu.className = "dropmenu";
    dropMenu.title = "MENU";
  } else {
    if (dropMenu.title == "MENU") {
      dropMenu.className = "hide";
      dropMenu.title = "CLOSED";
    }
  }
  var fetchRewards = document.getElementById("fetchRewards");
  var Mrewards = document.getElementById("minutes").innerHTML;
  var Srewards = document.getElementById("seconds").innerHTML;
  var Trewards = Mrewards + "." + Srewards;
  fetchRewards.innerHTML = Trewards;
}

function openGames() {
  var minyMenu = document.getElementById("gameMenu");

  if (minyMenu.title == "CLOSED") {
    minyMenu.className = "gameMenu";
    minyMenu.title = "Games";
  } else {
    if (minyMenu.title == "Games") {
      minyMenu.className = "hide";
      minyMenu.title = "CLOSED";
    }
  }
}

function slotsActive() {
  var linkItem = document.getElementById("SimpleSlots");

  if (linkItem.title == "Simple Slots") {
    linkItem.innerHTML == "ACTIVE";
    linkItem.title = "ACTIVE";
  } else {
    if (linkItem.title == "ACTIVE") {
      linkItem.innerHTML == "Simple Slots";
      linkItem.title = "Simple Slots";
    }
  }
}

